export function t(key: string, lang: Lang): string {
  return translations[lang]?.[key] ?? translations.fr[key] ?? key
}

export function tHtml(key: string, lang: Lang): { html: string; hasGradient: boolean } {
  const raw = t(key, lang)
  return {
    html: raw.replace(/<gradient>(.*?)<\/gradient>/g, '<span class="text-gradient">$1</span>'),
    hasGradient: raw.includes('<gradient>'),
  }
}
